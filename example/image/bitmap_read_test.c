#include <zx11/zximage.h>

int main(int argc, char *argv[])
{
  zxWindow win;
  zBitmap bitmap;
  zxImage img;
  const char *pattern[] = {
    "..........................................................",
    ".#################...................#############........",
    ".##################...............################........",
    ".###################............##################........",
    ".########.....#######..........###################........",
    "...######.....#######.........#######.......######........",
    "...######.....#######........#######......................",
    "...#################.........#######......................",
    "...################..........#######......................",
    "...#################.........#######......................",
    "...######.....#######........#######......................",
    "...######.....#######........#######......................",
    "...######.....#######.........#######.......######........",
    ".########.....#######..........###################........",
    ".########.....#######.######....##################.######.",
    ".########.....#######.######......################.######.",
    ".########.....#######.######.........#############.######.",
    "..........................................................",
    NULL,
  };

  zxInit();
  zBitmapRead( &bitmap, pattern, '#' );
  zxImageAllocDefault( &img, bitmap.width, bitmap.height );
  zBitmapToImage( &bitmap, &img );
  zxWindowCreateAndOpen( &win, 0, 0, img.width, img.height );
  zxImageDrawAll( &win, &img, 0, 0 );
  zxFlush();
  getchar();
  zxImageDestroy( &img );
  zBitmapDestroy( &bitmap );
  zxExit();
  return 0;
}
